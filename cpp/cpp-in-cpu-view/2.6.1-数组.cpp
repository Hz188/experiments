void func1(int a[2]) {
    a[0] = 1;
    a[1] = 2;
}

void func2(int* a) {
    *a = 1;
    *(a+1) = 2;
}

void func3(int* a) {
    a[0] = 1;
    a[1] = 2;
}

int main() {
    int a[2] {0, 1};
    func1(a);
    func2(a);
    func2(&a[0]);
    func3(a);

}
/*
    上面三个函数的汇编代码完全一致，下面三个函数调用的汇编代码也完全一致，也就说明了
        1. 数组类型作为参数传递的时候，会退化为指针类型，本质都是传递数组的内存首地址
        2. 数组的索引操作，其实等价于指针的解引用操作（语法优化）
    
*/